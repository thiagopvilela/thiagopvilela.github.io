document.getElementById('mobileMenuBtn').addEventListener('click', function() {
    document.querySelector('.sideHeader').style.display = 'flex';
});

document.querySelector('.sideHeaderBtn').addEventListener('click', function() {
    const sideHeader = document.querySelector('.sideHeader');
    if (sideHeader.style.display === 'none') {
      sideHeader.style.display = 'block';
    } else {
      sideHeader.style.display = 'none';
    }
  });





